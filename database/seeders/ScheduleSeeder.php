use App\Models\Schedule;
use App\Models\Course;

class ScheduleSeeder extends Seeder
{
    /**
     * Run the database seeds.
     */
    public function run(): void
    {
        $courses = Course::all();

        foreach ($courses as $course) 
        {
            Schedule::factory()->create([
                'course_id' => $course
            ]);
        }
    }
}
